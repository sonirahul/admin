function init_map() {
    var myOptions = {
        zoom: 12,
        scrollwheel:false,
        scaleControl:false,
        center: new google.maps.LatLng(29.37585899999999, 48.00223471640631),
        mapTypeId: google.maps.MapTypeId.ROADMAP
